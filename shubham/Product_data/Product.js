  const append = ( clothes) =>{

    let maindiv = document.getElementById("product") 
    maindiv.innerHTML = null;

    clothes.map(function(el){

      let card = document.createElement("div")
      card.setAttribute("id","card")
      card.addEventListener("click",function(){
      addlocalstorage(el)
      })

      let img = document.createElement("img")
      img.setAttribute("id",'img')
      img.src = el.img

      let name = document.createElement("h4")
      name.setAttribute("id",'name')
      name.textContent = el.name;

      let title = document.createElement("h4")
      title.setAttribute("id",'brand')
      title.textContent = el.title

      let category = document.createElement("h4")
      category.setAttribute('id','category')
      category.textContent = el.category

      let price = document.createElement("h3")
      price.setAttribute("id","price")
      price.textContent ="$"+el.price;

      let dis_cost = document.createElement("h4")
      dis_cost.setAttribute('id','discount')
      dis_cost.textContent = el. dis;

      card.append(img,name,title,category,price,dis_cost)
      
      maindiv.append(card);

      img.onmouseover = () => {
        if (el.img1 !== undefined) {
          img.src = el.img1;
        } 
        
      };

      img.onmouseout = () => {
        img.src = el.img;
     
      };

    })

  }
  append(clothes);

  function filter(event){

    let form = document.querySelector("select");
     
    let low = form.value;
    console.log(low);
    // console.log(clothes);
    
    if(low === "low"){
       clothes.sort(function(a,b){
        return (a.cost - b.cost);
      })
      append(clothes);
      
    } else if(low === "high"){
      clothes.sort(function(a,b){
        return (b.cost - a.cost);
      })
      append(clothes);
    } else if(low === 'A'){
      clothes.sort(function(a, b) { return (a.title > b.title ? 1 : (a.title === b.title ? 0 : -1)) 
      })
      append(clothes);
    } else if(low === 'Z'){
      clothes.sort(function(a, b) { return (a.title < b.title ? 1 : (a.title === b.title ? 0 : -1)) 
      })
      append(clothes);
    }
   
  }

